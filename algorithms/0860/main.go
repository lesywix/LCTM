func lemonadeChange(bills []int) bool {
	five, ten, twenty := 0, 0, 0
	if bills[0] > 5 {
		return false
	}
	for i := 0; i < len(bills); i++ {
		switch bills[i] {
		case 5:
			five++
		case 10:
			if five <= 0 {
				return false
			} else {
				ten++
				five--
			}
		case 20:
			if five > 0 && ten > 0 {
				five--
				ten--
				twenty++
			} else if five >= 3 {
				five-=3
				twenty++
			} else {
				return false
			}
		}
	}
	return true
}

